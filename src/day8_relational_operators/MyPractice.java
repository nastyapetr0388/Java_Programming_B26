package day8_relational_operators;

public class MyPractice {

    public static void main(String[] args) {

        int b = 10; //10->11
        b++;
        System.out.println(b);

        b--; // 11->10->10->11->12
        System.out.println(b);
        System.out.println(b++);
        System.out.println(b++);


    }

}
