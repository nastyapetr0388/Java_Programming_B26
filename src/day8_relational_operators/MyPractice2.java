package day8_relational_operators;

public class MyPractice2 {

    public static void main(String[] args) {

        int a = 40;// 40->39->38->37->36->35
        System.out.println(--a);
        System.out.println(--a);
        System.out.println(a--);
        System.out.println(--a);
        System.out.println(--a);
        System.out.println(a--);
    }
}
